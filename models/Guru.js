const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const guruSchema = new Schema({
    username: {
        type: String
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        required: true
    },
    created_at: {
        type: Date,
        default: Date.now
    },
    updated_at: {
        type: Date,
        default: Date.now
    }
});

module.exports = Guru = mongoose.model("Guru", guruSchema);
